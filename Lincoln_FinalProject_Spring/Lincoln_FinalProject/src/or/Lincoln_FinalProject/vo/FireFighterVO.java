package or.Lincoln_FinalProject.vo;

public class FireFighterVO {
	private int fireFighterCode,fireStationCode;
	private String IP,fireFighterName;
	public int getFireFighterCode() {
		return fireFighterCode;
	}
	public void setFireFighterCode(int fireFighterCode) {
		this.fireFighterCode = fireFighterCode;
	}
	public int getFireStationCode() {
		return fireStationCode;
	}
	public void setFireStationCode(int fireStationCode) {
		this.fireStationCode = fireStationCode;
	}
	public String getIP() {
		return IP;
	}
	public void setIP(String iP) {
		IP = iP;
	}
	public String getFireFighterName() {
		return fireFighterName;
	}
	public void setFireFighterName(String fireFighterName) {
		this.fireFighterName = fireFighterName;
	}
	
}
