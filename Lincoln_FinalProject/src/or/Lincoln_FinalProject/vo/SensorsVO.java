package or.Lincoln_FinalProject.vo;

public class SensorsVO {
	
	
	private String videoCode;
	private String gyroCode;
	private String temperatureCode;
	private String heartRateCode;
	private String smokeCode;
	
	public String getVideoCode() {
		return videoCode;
	}
	public void setVideoCode(String videoCode) {
		this.videoCode = videoCode;
	}
	public String getGyroCode() {
		return gyroCode;
	}
	public void setGyroCode(String gyroCode) {
		this.gyroCode = gyroCode;
	}
	public String getTemperatureCode() {
		return temperatureCode;
	}
	public void setTemperatureCode(String temperatureCode) {
		this.temperatureCode = temperatureCode;
	}
	public String getHeartRateCode() {
		return heartRateCode;
	}
	public void setHeartRateCode(String heartRateCode) {
		this.heartRateCode = heartRateCode;
	}
	public String getSmokeCode() {
		return smokeCode;
	}
	public void setSmokeCode(String smokeCode) {
		this.smokeCode = smokeCode;
	}
	
	
}
